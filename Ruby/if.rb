## if statement is a structure to help make decision and respond to values

I wake up 
If I am hungry
  I eat breakfast

I leave my house
if it is cloudy
  I bring an umbrella
otherwise
  I bring sunglasses

  I am at a restaurant
  if I want meat
    I order a steak
  otherwise if I want pasta
    I order spaghetti and meatballs
  otherwise
    I order a salad.
